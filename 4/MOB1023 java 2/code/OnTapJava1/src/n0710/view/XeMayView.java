    private void tblXeMayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblXeMayMouseClicked
        // TODO add your handling code here:
        int row = tblXeMay.getSelectedRow();
        XeMay xm = qlxm.getList().get(row);
        
        txtID.setText(xm.getID().toString());
        txtTen.setText(xm.getTen());
        txtDungTich.setText(xm.getDungTich().toString());
        
        cboChungLoai.setSelectedItem(xm.getChungLoai().toString());
    }//GEN-LAST:event_tblXeMayMouseClicked

    private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveMouseClicked
        // TODO add your handling code here:
        int row = tblXeMay.getSelectedRow();
        
        int confirm = JOptionPane.showConfirmDialog(this, "Ban co chac chan muon xoa khong?", "Xac nhan", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            String kq = qlxm.remove(row);
            JOptionPane.showMessageDialog(this, kq);
        }
        
        loadDataTable(qlxm.getList());
    }//GEN-LAST:event_btnRemoveMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        int row = tblXeMay.getSelectedRow();
        
        XeMay xm = qlxm.getList().get(row);
        
        Integer ID = Integer.parseInt(txtID.getText());
        String ten = txtTen.getText();
        String chungLoai = cboChungLoai.getSelectedItem().toString();
        Integer dungTich = Integer.parseInt(txtDungTich.getText());
        
        XeMay xm1 = new XeMay(ID, ten, chungLoai, dungTich);
        
        String kq = qlxm.update(row, xm1);
        JOptionPane.showMessageDialog(this, kq);
        
        loadDataTable(qlxm.getList());
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnWriteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnWriteMouseClicked
        // TODO add your handling code here:
        String path = "output.txt";
        ArrayList<XeMay> file = qlxm.getList();
        try {
            FileWriter fileWriter = new FileWriter(path);
            BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);
            for (XeMay xeMay : file) {
                bufferedWriter.write(xeMay.toString());
                bufferedWriter.newLine();
            }
            bufferedWriter.close();
        } catch (Exception e) {
            System.out.println("Co loi xay ra: " + e.getMessage());
        }
    }//GEN-LAST:event_btnWriteMouseClicked

