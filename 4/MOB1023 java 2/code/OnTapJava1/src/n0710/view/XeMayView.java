    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        if (txtID.getText().isEmpty() || txtTen.getText().isEmpty() || cboChungLoai.getSelectedItem().equals("Select") || txtDungTich.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Vui long dien day du thong tin");
        } else {
            try {
                Integer ID = Integer.parseInt(txtID.getText());
                String ten = txtTen.getText();
                String chungLoai = cboChungLoai.getSelectedItem().toString();
                Integer dungTich = Integer.parseInt(txtDungTich.getText());

                XeMay xm = new XeMay(ID, ten, chungLoai, dungTich);

                String kq = qlxm.add(xm);
                JOptionPane.showMessageDialog(this, kq);

                loadDataTable(qlxm.getList());

                txtID.setText("");
                txtTen.setText("");
                txtDungTich.setText("");
                cboChungLoai.setSelectedIndex(0);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "ID va Dung tich phai la so");
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

