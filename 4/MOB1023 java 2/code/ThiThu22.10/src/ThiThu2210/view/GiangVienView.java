    private void btnThemSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemSuaMouseClicked
        // TODO add your handling code here:
        int row = tblGiangVien.getSelectedRow();
        
        String ten = txtTen.getText().trim();
        String mon = cboMon.getSelectedItem().toString();
        
        try {
            Double luong = Double.valueOf(txtLuong.getText().trim());
            
            if (ten.isEmpty() ||
                mon.equals("Select")) {
                JOptionPane.showMessageDialog(null, "Vui long khong de trong thong tin");
            } else if (luong < 10) {
                JOptionPane.showMessageDialog(null, "Luong khong duoc be hon 10");
            } else {
                GiangVien gv = new GiangVien(ten, mon, luong);

                if (row == -1) {
                    int confirm = JOptionPane.showConfirmDialog(null, "Ban co chac chan muon them khong", "Xac nhan", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                        String kq = qlgv.them(gv);
                        JOptionPane.showMessageDialog(this, kq);
                    }
                } else {
                    int confirm = JOptionPane.showConfirmDialog(null, "Ban co chac chan muon cap nhat khong", "Xac nhan", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                        String kq = qlgv.sua(row, gv);
                        JOptionPane.showMessageDialog(this, kq);
                    }
                }

                loadData(qlgv.getList());
                clearForm();
            }
                
        } catch (NumberFormatException e) {
            System.out.println("Luong phai la so hop le");
        } catch (Exception e) {
            System.out.println("Co loi xay ra: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemSuaMouseClicked

