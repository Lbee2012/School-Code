    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        if (txtID.getText().isEmpty() || txtName.getText().isEmpty() || txtAge.getText().isEmpty() || cboRole.getSelectedItem().equals("Select") || !rdCo.isSelected() && !rdKo.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui long dien day du thong tin");
        } else {
            try {
                Integer ID = Integer.parseInt(txtID.getText());
                String name = txtName.getText();
                String age = txtAge.getText();
                String role = cboRole.getSelectedItem().toString();
                String status;

                if (rdCo.isSelected()) {
                    status = "Dang lam viec";
                } else {
                    status = "Khong lam viec";
                }

                Staff s = new Staff(ID, name, ID, role, status);

                int confirm = JOptionPane.showConfirmDialog(null, "Ban co chac chan muon them khong", "Xac nhan", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    String kq = ss.add(s);
                    JOptionPane.showMessageDialog(this, kq);
                }

                loadData(ss.getList());
            } catch (Exception e) {
                System.out.println("Co loi xay ra: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        int row = tblStaff.getSelectedRow();
        Staff staff = ss.getList().get(row);
        
        Integer ID = Integer.parseInt(txtID.getText());
        String name = txtName.getText();
        String age = txtAge.getText();
        String role = cboRole.getSelectedItem().toString();
        String status;
        
        if (rdCo.isSelected()) {
            status = "Dang lam viec";
        } else {
            status = "Khong lam viec";
        }
        
        Staff s = new Staff(ID, name, ID, role, status);
        
        int confirm = JOptionPane.showConfirmDialog(this, "Ban co chac chan muon cap nhat khong", "Xac nhan", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String kq = ss.update(row, s);
            JOptionPane.showMessageDialog(this, kq);
        }
        
        loadData(ss.getList());
    }//GEN-LAST:event_btnUpdateMouseClicked

