    private void tblStaffMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStaffMouseClicked
        // TODO add your handling code here:
        int row = tblStaff.getSelectedRow();
        Staff s = ss.getList().get(row);
        
        txtID.setText(String.valueOf(s.getID()));
        txtName.setText(String.valueOf(s.getName()));
        txtAge.setText(String.valueOf(s.getAge()));
        
        cboRole.setSelectedItem(s.getRole());
                
        if (s.getStatus().equals("Dang lam viec")) {
            rdCo.setSelected(true);
        } else {
            rdKo.setSelected(true);
        }
    }//GEN-LAST:event_tblStaffMouseClicked

