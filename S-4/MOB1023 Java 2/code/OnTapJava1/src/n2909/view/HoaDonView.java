    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        //b1: lay thong tin tren form
        String ID = txtID.getText();
        String sanPham = txtSanPham.getText();
        int soLuong = Integer.parseInt(txtSoLuong.getText());
        float giaBan = Float.parseFloat(txtGiaBan.getText());
        String trangThai;
        
        if (rdConHang.isSelected()) {
            trangThai = "Con hang";
        } else {
            trangThai = "Het hang";
        }
        
        //b2: tao doi tuong tu thong tin lay duoc
        HoaDon hd = new HoaDon(ID, sanPham, soLuong, giaBan, trangThai);

        //b3: them doi tuong vao danh sach
        String kq = qlhd.them(hd);
        JOptionPane.showMessageDialog(this, kq);

        //b4: auto reload
        loadDataTable(qlhd.getList());
    }//GEN-LAST:event_btnThemMouseClicked

