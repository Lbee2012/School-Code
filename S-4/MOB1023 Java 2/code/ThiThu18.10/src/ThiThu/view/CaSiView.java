    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        String ma = txtMa.getText().trim();
        String ten = txtTen.getText().trim();
        Integer namHoatDong = Integer.valueOf(txtNamHoatDong.getText().trim());
        String gioiTinh;
        if (rdNam.isSelected()) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nu";
        }
        String hang = cboHang.getSelectedItem().toString();
        
        if (ma.isEmpty() || 
            ten.isEmpty() ||
            !rdNam.isSelected() && !rdNu.isSelected() ||
            cboHang.getSelectedItem().equals("Select") || 
            namHoatDong.toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui long dien day du thong tin");
        } else if (ten.matches(".*\\d.*")) { // Kiểm tra tên có chứa số - Thanks to ChatGPT
            JOptionPane.showMessageDialog(null, "Ten khong duoc chua so");
        } else if (namHoatDong >= 100) {
            JOptionPane.showMessageDialog(null, "Nam hoat dong khong duoc lon hon 100");
        } else {
            try {
                CaSi cs = new CaSi(ma, ten, gioiTinh, hang, namHoatDong);

                int confirm = JOptionPane.showConfirmDialog(null, "Ban co chac chan muon them khong", "Xac nhan", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    String kq = qlcs.add(cs);
                    JOptionPane.showMessageDialog(this, kq);
                }

                loadData(qlcs.getList());
            } catch (NumberFormatException e) {
                System.out.println("Co loi xay ra: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        int row = tblCaSi.getSelectedRow();
        
        String ma = txtMa.getText().trim();
        String ten = txtTen.getText().trim();
        Integer namHoatDong = Integer.valueOf(txtNamHoatDong.getText().trim());
        String gioiTinh;
        if (rdNam.isSelected()) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nu";
        }
        String hang = cboHang.getSelectedItem().toString();
        
        CaSi cs = new CaSi(ma, ten, gioiTinh, hang, namHoatDong);
        
        int confirm = JOptionPane.showConfirmDialog(null, "Ban co chac chan muon cap nhat khong", "Xac nhan", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String kq = qlcs.update(row, cs);
            JOptionPane.showMessageDialog(this, kq);
        }
        
        loadData(qlcs.getList());
    }//GEN-LAST:event_btnUpdateMouseClicked

