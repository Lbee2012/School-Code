    void loadData(ArrayList<CaSi> csList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblCaSi.getModel();
        tableModel.setRowCount(0);
        
        for (CaSi caSi : csList) {
            tableModel.addRow(new Object[] {
                caSi.getMa(),
                caSi.getTen(),
                caSi.getGioiTinh(),
                caSi.getHang(),
                caSi.getNamHoatDong()
            });
        }
    }

