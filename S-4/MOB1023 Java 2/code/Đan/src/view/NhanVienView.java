    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblNhanVien.getSelectedRow();

            String id = txtID.getText();
            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ma nhan vien khong duoc de trong");
                return;
            }

            String hoTen = txtHoten.getText();
            if (hoTen.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ho ten nhan vien khong duoc de trong");
                return;
            }

            Integer tuoi = Integer.valueOf(txtTuoi.getText());
            if (tuoi < 16 || tuoi > 55) {
                JOptionPane.showMessageDialog(this, "Tuoi phai tu 16 den 55");
                return;
            }

            String email = txtEmail.getText();
            if (email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Email nhan vien khong duoc de trong");
                return;
            }

            Integer luong = Integer.valueOf(txtLuong.getText());
            if (luong < 5000000) {
                JOptionPane.showMessageDialog(this, "Luong phai tren 5 trieu");
                return;
            }

            NhanVien nv = new NhanVien(id, hoTen, tuoi, email, luong);

            if (row >= 0) {
                String kq = qlnv.save(row, nv);
                JOptionPane.showMessageDialog(this, kq);
            } else {
                String kq = qlnv.them(nv);
                JOptionPane.showMessageDialog(this, kq);
            }

            loadData(qlnv.getlist());
            clearForm();
        } catch (Exception e) {
            System.out.println("Co loi xay ra: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveMouseClicked

