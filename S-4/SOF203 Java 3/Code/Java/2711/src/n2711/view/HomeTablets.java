    private void tblTabletsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTabletsMouseClicked
        // TODO add your handling code here:
        int row = tblTablets.getSelectedRow();
        
        Integer ID = (Integer) tblTablets.getValueAt(row, 0);
        Tablets t = repositoryTablets.details(ID);
        
        txtTabletID.setText(String.valueOf(t.getTabletID()));
        txtBrand.setText(String.valueOf(t.getBrand()));
        txtModel.setText(String.valueOf(t.getModel()));
        txtScreenSize.setText(String.valueOf(t.getScreenSize()));
        txtStorage.setText(String.valueOf(t.getStorage()));
    }//GEN-LAST:event_tblTabletsMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        Integer tabletID = Integer.valueOf(txtTabletID.getText());
        String brand = txtBrand.getText();
        String model = txtModel.getText();
        Double screenSize = Double.valueOf(txtScreenSize.getText());
        Integer storage = Integer.valueOf(txtStorage.getText());
        
        Tablets t = new Tablets(tabletID, brand, model, screenSize, storage);
        Boolean kq = repositoryTablets.add(t);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Them tablet moi thanh cong!");
            loadData(repositoryTablets.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them tablet moi that bai!");
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        Integer id = Integer.valueOf(txtTabletID.getText());
        
        Integer tabletID = Integer.valueOf(txtTabletID.getText());
        String brand = txtBrand.getText();
        String model = txtModel.getText();
        Double screenSize = Double.valueOf(txtScreenSize.getText());
        Integer storage = Integer.valueOf(txtStorage.getText());
        
        Tablets t = new Tablets(tabletID, brand, model, screenSize, storage);
        Boolean kq = repositoryTablets.update(t, id);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Cap nhat thanh cong thong tin tablet co ID: " + id + " !");
            loadData(repositoryTablets.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Cap nhat that bai thong tin tablet co ID: " + id + " !");
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveMouseClicked
        // TODO add your handling code here:
        Integer id = Integer.valueOf(txtTabletID.getText());
        Boolean kq = repositoryTablets.remove(id);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Xoa thanh cong tablet co ID: " + id);
            loadData(repositoryTablets.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Khong tim duoc tablet nao co ID: " + id);
        }
    }//GEN-LAST:event_btnRemoveMouseClicked

    private void btnStorageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnStorageMouseClicked
        // TODO add your handling code here:
        Integer min = Integer.valueOf(cboMin.getSelectedItem().toString());
        Integer max = Integer.valueOf(cboMax.getSelectedItem().toString());
        
        ArrayList<Tablets> filtered = repositoryTablets.findTabletsByStorage(min, max);
        
        loadDataStorage(filtered);
    }//GEN-LAST:event_btnStorageMouseClicked

