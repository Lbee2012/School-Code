    void loadData(ArrayList<Tablets> tList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblTablets.getModel();
        tableModel.setRowCount(0);
        
        for (Tablets t : tList) {
            tableModel.addRow(new Object[] {
                t.getTabletID(),
                t.getBrand(),
                t.getModel(),
                t.getScreenSize(),
                t.getStorage(),
            });
        }
    }
    
    void loadDataStorage(ArrayList<Tablets> filtered) {
        DefaultTableModel tableModel = (DefaultTableModel) tblTablets.getModel();
        tableModel.setRowCount(0);
        
        for (Tablets t : filtered) {
            tableModel.addRow(new Object[] {
                t.getTabletID(),
                t.getBrand(),
                t.getModel(),
                t.getScreenSize(),
                t.getStorage(),
            });
        }
    }

