    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        int row = tblNhanVien.getSelectedRow();
        String ma = (String) tblNhanVien.getValueAt(row, 0);
        
        NhanVien nv = repositoryNV.details(ma);
        txtMa.setText(String.valueOf(nv.getMa()));
        txtTen.setText(String.valueOf(nv.getTen()));
        txtSDT.setText(String.valueOf(nv.getSdt()));
//        if (nv.getTrangThai().equals(true)) {
//            cboTrangThai.setSelectedIndex(1);
//        } else {
//            cboTrangThai.setSelectedIndex(2);
//        }
        cboTrangThai.setSelectedItem(nv.getTrangThai() ? "Hoat dong" : "Khong hoat dong");
    }//GEN-LAST:event_tblNhanVienMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        if (txtMa.getText().isEmpty() ||
                txtTen.getText().isEmpty() ||
                txtSDT.getText().isEmpty() ||
                cboTrangThai.getSelectedItem().equals("Select")) {
            JOptionPane.showMessageDialog(this, "Vui long khong de trong bat ki thong tin nao!");
        } else {
            String ma = txtMa.getText().trim();
            String ten = txtTen.getText().trim();
            String sdt = txtSDT.getText().trim();
            Boolean trangThai;
            if (cboTrangThai.equals("Hoat dong")) {
                trangThai = true;
            } else {
                trangThai = false;
            }

            NhanVien nv = new NhanVien(ma, ten, sdt, trangThai);
            Boolean kq = repositoryNV.add(nv);

            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Them thanh cong!");
                loadData(repositoryNV.getList());
            } else {
                JOptionPane.showMessageDialog(this, "Them that bai");
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveMouseClicked
        // TODO add your handling code here:
        String id = txtMa.getText().trim();
        
        Boolean kq = repositoryNV.remove(id);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Xoa thanh cong!");
            loadData(repositoryNV.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Xoa that bai");
        }
    }//GEN-LAST:event_btnRemoveMouseClicked

    private void btnClearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnClearMouseClicked
        // TODO add your handling code here:
        txtMa.setText("");
        txtTen.setText("");
        txtSDT.setText("");
        cboTrangThai.setSelectedIndex(0);
        
        tblNhanVien.clearSelection();
        
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
    }//GEN-LAST:event_btnClearMouseClicked

