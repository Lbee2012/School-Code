    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        int row = tblSanPham.getSelectedRow();
        String ma = (String) tblSanPham.getValueAt(row, 0);
        SanPham sp = repositorySP.details(ma);
        
        txtMa.setText(String.valueOf(sp.getMa()));
        txtTen.setText(String.valueOf(sp.getTen()));
        txtGia.setText(String.valueOf(sp.getGiaBan()));
        if (sp.getTrangThai().equals(true)) {
            rdCon.setSelected(true);
        } else {
            rdHet.setSelected(true);
        }
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        String ma = txtMa.getText().trim();
        String ten = txtTen.getText().trim();
        Double giaBan = Double.valueOf(txtGia.getText().trim());
        Boolean trangThai;
        if (rdCon.isSelected()) {
            trangThai = true;
        } else {
            trangThai = false;
        }
        
        SanPham sp = new SanPham(ma, ten, giaBan, trangThai);
        Boolean kq = repositorySP.add(sp);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            loadData(repositorySP.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them that bai");
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        String ma = txtMa.getText().trim();
        String ten = txtTen.getText().trim();
        Double giaBan = Double.valueOf(txtGia.getText().trim());
        Boolean trangThai;
        if (rdCon.isSelected()) {
            trangThai = true;
        } else {
            trangThai = false;
            giaBan = 0.0;
        }
        
        SanPham sp = new SanPham(ma, ten, giaBan, trangThai);
        
        int confirm = JOptionPane.showConfirmDialog(this, "Ban co chac chan muon sua khong?", "Xac nhan", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            Boolean kq = repositorySP.update(sp);
            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Cap nhat thanh cong");
                loadData(repositorySP.getList());
            } else {
                JOptionPane.showMessageDialog(this, "Cap nhat that bai");
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // TODO add your handling code here:
        Double min = Double.valueOf(txtMin.getText().trim());
        Double max = Double.valueOf(txtMax.getText().trim());
        
        ArrayList<SanPham> kq = repositorySP.search(min, max);
        if (kq.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Khong tim duoc");
        } else {
            JOptionPane.showMessageDialog(this, "Tim duoc");
            loadData(repositorySP.search(min, max));
        }
    }//GEN-LAST:event_btnSearchMouseClicked

