    private void tblUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsersMouseClicked
        // TODO add your handling code here:
        int row = tblUsers.getSelectedRow();
        String id = (String) tblUsers.getValueAt(row, 0);
        Users users = re.tblUsers(id);
        txtID.setText(users.getId());
        txtPass.setText(users.getPass());
        txtName.setText(users.getName());
        txtEmail.setText(users.getEmail());
        if (users.isAdmin() == true) {
            rd1.setSelected(true);
        } else {
            rdd0.setSelected(true);
        }
    }//GEN-LAST:event_tblUsersMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        String id = txtID.getText();
        String pass = txtPass.getText();
        String name = txtName.getText();
        String email = txtEmail.getText();
        boolean admin;
        if (rd1.isSelected()) {
            admin = true;
        } else {
            admin = false;
        }
        Users users = new Users(id, pass, name, email, admin);
        boolean kq = re.add(users);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            loadData(re.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them that bai");
        }
    }//GEN-LAST:event_btnAddMouseClicked

