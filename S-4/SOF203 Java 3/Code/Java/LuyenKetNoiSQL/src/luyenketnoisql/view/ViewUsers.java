    public void loadData(ArrayList<Users> list){
        DefaultTableModel tableModel = (DefaultTableModel) tblUsers.getModel();
        tableModel.setRowCount(0);
        for (Users users : list) {
            tableModel.addRow(new Object[]{
                users.getId(),
                users.getPass(),
                users.getName(),
                users.getEmail(),
                users.isAdmin() ? 1 : 0
            });
        }
    }

