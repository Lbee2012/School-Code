    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // TODO add your handling code here:
        Double min = Double.valueOf(txtMin.getText().trim());
        Double max = Double.valueOf(txtMax.getText().trim());
        
        ArrayList<SanPham> kq = repository.search(min, max);
        if (kq.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Khong tim duoc san pham nao co gia trong khoang " + min + " den " + max);
        } else {
            JOptionPane.showMessageDialog(this, "Tim duoc " + kq.size() + " san pham co gia trong khoang tu " + min + " den " + max);
            loadData(repository.search(min, max));
        }
    }//GEN-LAST:event_btnSearchMouseClicked

