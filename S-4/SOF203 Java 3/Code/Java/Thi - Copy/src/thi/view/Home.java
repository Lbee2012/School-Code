    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        int row = tblSanPham.getSelectedRow();
        String maSP = (String) tblSanPham.getValueAt(row, 0);
        SanPham sp = repository.details(maSP);
        
        txtMa.setText(String.valueOf(sp.getMa()));
        txtTen.setText(String.valueOf(sp.getTen()));
        if (sp.getTrangThai().equals(true)) {
            rdConHang.setSelected(true);
        } else {
            rdHetHang.setSelected(true);
        }
        txtGia.setText(String.valueOf(sp.getGiaBan()));
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        Double gia = Double.valueOf(txtGia.getText().trim());
        
        if (txtMa.getText().isEmpty() ||
                txtTen.getText().isEmpty() ||
                !rdConHang.isSelected() && !rdHetHang.isSelected() ||
                txtGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui long khong de trong thong tin!");
        } else {
            String ma = txtMa.getText().trim();
            String ten = txtTen.getText().trim();
            Boolean trangThai;
            if (rdConHang.isSelected()) {
                trangThai = true;
            } else {
                trangThai = false;
            }
            Double giaBan = Double.valueOf(txtGia.getText().trim());

            SanPham sp = new SanPham(ma, ten, trangThai, giaBan);

            int confirm = JOptionPane.showConfirmDialog(this, "Ban co chac chan muon them khong?", "Xac nhan", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                Boolean kq = repository.add(sp);
                if (kq == true) {
                    JOptionPane.showMessageDialog(this, "Them san pham moi thanh cong!");
                    loadData(repository.getList());
                } else {
                    JOptionPane.showMessageDialog(this, "Them san pham moi that bai!");
                }
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        if (txtMa.getText().isEmpty() ||
                txtTen.getText().isEmpty() ||
                !rdConHang.isSelected() && !rdHetHang.isSelected() ||
                txtGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui long khong de trong thong tin!");
        } else {
            String ma = txtMa.getText().trim();
            String ten = txtTen.getText().trim();
            Boolean trangThai;
            if (rdConHang.isSelected()) {
                trangThai = true;
            } else {
                trangThai = false;
                txtGia.setText("0");
            }
            Double giaBan = Double.valueOf(txtGia.getText().trim());

            SanPham sp = new SanPham(ma, ten, trangThai, giaBan);

            int confirm = JOptionPane.showConfirmDialog(this, "Ban co chac chan muon sua khong?", "Xac nhan", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                Boolean kq = repository.update(sp);
                if (kq == true) {
                    JOptionPane.showMessageDialog(this, "Cap nhat thong tin san pham thanh cong!");
                    loadData(repository.getList());
                } else {
                    JOptionPane.showMessageDialog(this, "Cap nhat thong tin san pham that bai!");
                }
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

